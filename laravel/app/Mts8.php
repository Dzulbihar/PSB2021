<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class Mts8 extends Model
{
    protected $table = 'mts8';
	protected $fillable = ['user_id','namalengkap38','status38','avatar38','nisn38','nik38','tempatlahir38','tbt38','jenkel38','desa38','kec38','kab38','prov38','kodepos38','cita38','jumlahsaudara38','asalsekolah38','alamatasalsekolah38','nokk38','namaayah38','nikayah38','tahunlahirayah38','pendidikanayah38','pekerjaanayah38','namaibu38','nikibu38','tahunlahiribu38','pendidikanibu38','pekerjaanibu38','penghasilan38','prestasi38','nohp38'];

//tambah foto
	public function getAvatar()
	{
		if(!$this->avatar38){
			return asset('images/default.jpg');
		}			
		return asset('images/' .$this->avatar38);
	}
//tambah foto

	public function mapel()
	{
		return $this->belongsToMany(Mapel::class)->withPivot(['nilai'])->withTimeStamps();
	}

	public function hukuman()
	{
		return $this->belongsToMany(Hukuman::class)->withPivot(['jenis', 'sanksi', 'skor',  'data'])->withTimeStamps();
	}
 

	public function izin()
	{
		return $this->belongsToMany(Izin::class)->withPivot(['rumah','jangka','pondok','datang','hukum','ket'])->withTimeStamps();
	}
 
	public function ambil()
	{
		return $this->belongsToMany(Ambil::class)->withPivot(['pelunasan','pengambilan','keterangan'])->withTimeStamps();
	}

	public function prestasi()
	{
		return $this->belongsToMany(Prestasi::class)->withPivot(['tingkat','kategori','hadiah','waktu','note'])->withTimeStamps();
	}

	public function catatan()
	{
		return $this->belongsToMany(Catatan::class)->withPivot(['sifat','nulis'])->withTimeStamps();
	}

//dashboard
	public function rataRataNilai()
	{
		//ambil nilai rata2
		$total = 0;
		$hitung = 0;
		foreach($this->mapel as $mapel){
			$total += $mapel->pivot->nilai;
			$hitung++;;
		}

		return round($total);
		//hitung rata2 nilai
		//return round($total/$hitung);		
	}

